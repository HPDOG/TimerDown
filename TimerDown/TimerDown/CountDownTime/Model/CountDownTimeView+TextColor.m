@implementation CountDownTimeView (TextColor)
+(UIColor*)daysLabel_0WithTextColor:(CountDownTimeType)type{
    UIColor *color = [[UIColor alloc] init];
    switch (type) {
        case CountDownTimeType_Normal:
            color = RGB_COLOR(255, 255, 255);
            break;
        case CountDownTimeType_Word:
            color = RGB_COLOR(255, 255, 255);
            break;
        case CountDownTimeType_Word_AD_MAIN:
            color = RGB_COLOR(255, 255, 255);
            break;
        case CountDownTimeType_FlipNoRepeat:
            color = RGB_COLOR(255, 255, 255);
            break;
        default:
            break;
    }
    return color;
}

