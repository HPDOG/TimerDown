@implementation CountDownTimeView (BGColor)
+(UIColor*)daysLabel_0WithBGColor:(CountDownTimeType)type{
    UIColor *color = [[UIColor alloc] init];
    switch (type) {
        case CountDownTimeType_Normal:
            color = [UIColor clearColor];
            break;
        case CountDownTimeType_Word:
            color = [UIColor clearColor];
            break;
        case CountDownTimeType_Word_AD_MAIN:
            color = [UIColor clearColor];
            break;
        case CountDownTimeType_FlipNoRepeat:
            color = RGB_COLOR(0, 0, 0);
            break;
        default:
            break;
    }
    return color;
}

